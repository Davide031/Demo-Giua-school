namespace App\Tests\UnitTest\Entity;

use App\DataFixtures\ConfigurazioneFixtures;
use App\Tests\DatabaseTestCase;


/**
 * Unit test della classe
 */
class ConfigurazioneTest extends DatabaseTestCase {

  /**
   * Costruttore
   * Definisce dati per i test.
   *
   */
  public function __construct() {
    parent::__construct();
    // nome dell'entità
    $this->entity = '\App\Entity\Configurazione';
    // campi da testare
    $this->fields = ['categoria', 'parametro', 'descrizione', 'valore', 'gestito'];
    // fixture da caricare
    $this->fixtures = [ConfigurazioneFixtures::class];
    // SQL read
    $this->canRead = ['gs_configurazione' => ['id', 'creato', 'modificato', 'categoria', 'parametro',
      'descrizione', 'valore', 'gestito']];
    // SQL write
    $this->canWrite = ['gs_configurazione' => ['id', 'creato', 'modificato', 'categoria', 'parametro',
      'descrizione', 'valore', 'gestito']];
    // SQL exec
    $this->canExecute = ['START TRANSACTION', 'COMMIT'];
  }

  /**
   * Test getter/setter degli attributi, con memorizzazione su database.
   * Sono esclusi gli attributi ereditati.
   */
  public function testAttributi() {
    // carica oggetto esistente
    $existent = $this->em->getRepository($this->entity)->find(1);
    $this->assertEquals(1, $existent->getId(), 'Oggetto esistente');
    // crea nuovi oggetti
    for ($i = 0; $i < 3; $i++) {
      $o[$i] = new $this->entity();
      foreach ($this->fields as $field) {
        $data[$i][$field] =
          $field == 'categoria' ? ucfirst($this->faker->words(1, true)) :
          ($field == 'parametro' ? implode('_', array_map('strtolower', $this->faker->unique()->words(3))) :
          ($field == 'descrizione' ? $this->faker->paragraph(1, true) :
          ($field == 'valore' ? $this->faker->words(1, true) :
          $this->faker->randomElement([false, false, true]))));
        $o[$i]->{'set'.ucfirst($field)}($data[$i][$field]);
      }
      $this->assertEmpty($o[$i]->getId(), $this->entity.'::getId Pre-inserimento');
      $this->assertEmpty($o[$i]->getCreato(), $this->entity.'::getCreato Pre-inserimento');
      $this->assertEmpty($o[$i]->getModificato(), $this->entity.'::getModificato Pre-inserimento');
      // memorizza su db
      $this->em->persist($o[$i]);
      $this->em->flush();
      $this->assertNotEmpty($o[$i]->getId(), $this->entity.'::getId Post-inserimento');
      $this->assertNotEmpty($o[$i]->getCreato(), $this->entity.'::getCreato Post-inserimento');
      $this->assertNotEmpty($o[$i]->getModificato(), $this->entity.'::getModificato Post-inserimento');
      $data[$i]['id'] = $o[$i]->getId();
      $data[$i]['creato'] = $o[$i]->getCreato();
      // controlla creato < modificato
      sleep(1);
      $o[$i]->{'set'.ucfirst($this->fields[0])}(!$data[$i][$this->fields[0]]);
      $this->em->flush();
      $o[$i]->{'set'.ucfirst($this->fields[0])}($data[$i][$this->fields[0]]);
      $this->em->flush();
      $this->assertTrue($o[$i]->getCreato() < $o[$i]->getModificato(), $this->entity.'::getCreato < getModificato');
      $data[$i]['modificato'] = $o[$i]->getModificato();
    }
    // controlla gli attributi
    for ($i = 0; $i < 3; $i++) {
      $created = $this->em->getRepository($this->entity)->find($data[$i]['id']);
      foreach (array_merge(['id', 'creato', 'modificato'], $this->fields) as $field) {
        $this->assertSame($data[$i][$field], $created->{'get'.ucfirst($field)}(),
          $this->entity.'::get'.ucfirst($field));
      }
    }
    // controlla metodi setId, setCreato e setModificato
    $rc = new \ReflectionClass($this->entity);
    $this->assertFalse($rc->hasMethod('setId'), 'Esiste metodo '.$this->entity.'::setId');
    $this->assertFalse($rc->hasMethod('setCreato'), 'Esiste metodo '.$this->entity.'::setCreato');
    $this->assertFalse($rc->hasMethod('setModificato'), 'Esiste metodo '.$this->entity.'::setModificato');
  }

  /**
   * Test altri metodi
   */
  public function testMetodi() {
    // carica oggetto esistente
    $existent = $this->em->getRepository($this->entity)->find(1);
    // toString
    $this->assertSame($existent->getParametro().' = '.$existent->getValore(), (string) $existent, $this->entity.'::toString');
  }

  /**
   * Test validazione dei dati
   */
  public function testValidazione() {
    // carica oggetto esistente
    $existent = $this->em->getRepository($this->entity)->find(1);
    $this->assertCount(0, $this->val->validate($existent), $this->entity.' - Oggetto valido');
    // categoria
    $existent->setCategoria(null);
    $err = $this->val->validate($existent);
    $this->assertTrue(count($err) == 1 && $err[0]->getMessageTemplate() == 'field.notblank', $this->entity.'::categoria - NOT BLANK');
    $existent->setCategoria('123456789012345678901234567890123');
    $err = $this->val->validate($existent);
    $this->assertTrue(count($err) == 1 && $err[0]->getMessageTemplate() == 'field.maxlength', $this->entity.'::categoria - MAX LENGTH');
    $existent->setCategoria('12345678901234567890123456789012');
    $this->assertCount(0, $this->val->validate($existent), $this->entity.'::categoria - VALID MAX LENGTH');
    // parametro
    $existent->setParametro(null);
    $err = $this->val->validate($existent);
    $this->assertTrue(count($err) == 1 && $err[0]->getMessageTemplate() == 'field.notblank', $this->entity.'::parametro - NOT BLANK');
    $existent->setParametro('12345678901234567890123456789012345678901234567890123456789012345');
    $err = $this->val->validate($existent);
    $this->assertTrue(count($err) == 1 && $err[0]->getMessageTemplate() == 'field.maxlength', $this->entity.'::parametro - MAX LENGTH');
    $existent->setParametro('1234567890123456789012345678901234567890123456789012345678901234');
    $this->assertCount(0, $this->val->validate($existent), $this->entity.'::parametro - VALID MAX LENGTH');
    // descrizione
    $existent->setDescrizione(str_repeat('-', 1024).'X');
    $err = $this->val->validate($existent);
    $this->assertTrue(count($err) == 1 && $err[0]->getMessageTemplate() == 'field.maxlength', $this->entity.'::descrizione - MAX LENGTH');
    $existent->setDescrizione(str_repeat('-', 1024));
    $this->assertCount(0, $this->val->validate($existent), $this->entity.'::descrizione - VALID MAX LENGTH');
    // unique - parametro
    $this->em->flush();
    $o = $this->em->getRepository($this->entity)->find(2);
    $this->assertCount(0, $this->val->validate($o), $this->entity.' - Oggetto valido');
    $o->setParametro($existent->getParametro());
    $err = $this->val->validate($o);
    $this->assertTrue(count($err) == 1 && $err[0]->getMessageTemplate() == 'field.unique', $this->entity.'::parametro - UNIQUE');
  }

}
